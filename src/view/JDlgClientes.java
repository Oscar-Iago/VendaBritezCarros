    public OibClientes viewBean() {
        // TODO add your handling code here:
        OibClientes oibclientes = new OibClientes(); // criou o bean
        SimpleDateFormat df = new SimpleDateFormat("dd/mm/yyyy");
        int id = Integer.valueOf(jTxtCodigo.getText());
        oibclientes.setIdClientes(id);
        oibclientes.setNome(jTxtNome.getText());
        oibclientes.setSobrenome(jTxtSobrenome.getText());
        try {
            oibclientes.setDataNascimento(df.parse(jFmtNascimento.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgClientes.class.getName()).log(Level.SEVERE, null, ex);
        }

        oibclientes.setEmail(jTxtEmail.getText());
        oibclientes.setTelefone(Integer.valueOf(jTxtTelefone.getText()));
        //oibclientes.setSenha(jPwfSenha.getText());
        oibclientes.setCpfCnpj(jFmtCpf.getText());
        oibclientes.setRg(jTxtRg.getText());
        oibclientes.setStatus(jTxtStatus.getText());
        oibclientes.setEndereco(jTxtEndereco.getText());
        oibclientes.setCep(jTxtCep.getText());
        String eC = jCboEstado.getSelectedItem().toString();
        oibclientes.setEstadoCivil(eC);
        String S = jCboSexo.getSelectedItem().toString();
        oibclientes.setSexo(S);
        oibclientes.setNaturalidade(jTxtNaturalidade.getText());
        String tC = jCboTipo.getSelectedItem().toString();
        oibclientes.setTipoCliente(tC);
        return oibclientes;
    }

    public void beanView(OibClientes oibclientes) {
        String cad = String.valueOf(oibclientes.getIdClientes());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtNascimento.setText(formato.format(oibclientes.getDataNascimento()));

        jTxtCodigo.setText(cad);
        jTxtNome.setText(String.valueOf(oibclientes.getNome()));
        jTxtSobrenome.setText(oibclientes.getSobrenome());

        jTxtEmail.setText(oibclientes.getEmail());
        jTxtTelefone.setText(Integer.toString(oibclientes.getTelefone()));
        //jPwfSenha.setText(oibclientes.getSenha());
        jFmtCpf.setText(oibclientes.getCpfCnpj());
        jTxtRg.setText(oibclientes.getRg());
        jTxtStatus.setText(oibclientes.getStatus());
        jTxtEndereco.setText(oibclientes.getEndereco());
        jTxtCep.setText((oibclientes.getCep()));

        jCboEstado.setSelectedItem(oibclientes.getEstadoCivil());
        jCboSexo.setSelectedItem(oibclientes.getSexo());
        jTxtNaturalidade.setText(oibclientes.getNaturalidade());
        jCboTipo.setSelectedItem(oibclientes.getTipoCliente());

    }

