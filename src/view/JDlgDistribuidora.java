    public OibDistribuidora viewBean() {
        // TODO add your handling code here:
        OibDistribuidora oibdistribuidora = new OibDistribuidora(); // criou o bean
        SimpleDateFormat df = new SimpleDateFormat("dd/mm/yyyy");
        
        int id = Integer.valueOf(jTxtCodigo.getText());
        int quantidade = Integer.valueOf(jTxtQuantidade.getText());
        int preco = Integer.valueOf(jTxtPreco.getText());
        
        oibdistribuidora.setIdDistribuidora(id);
        //oibdistribuidora.setOibQuantidade(quantidade);
        //oibdistribuidora.setOibPreco(preco);
        oibdistribuidora.setOibNome(jTxtNome.getText());
        oibdistribuidora.setOibCarro(jTxtCarro.getText());

        try {
            oibdistribuidora.setOibTempoEntrega(df.parse(jFmtEntrega.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgDistribuidora.class.getName()).log(Level.SEVERE, null, ex);
        }
        return oibdistribuidora;
    }

    public void beanView(OibDistribuidora oibdistribuidora) {
        String cad = String.valueOf(oibdistribuidora.getIdDistribuidora());
        String quan = String.valueOf(oibdistribuidora.getOibQuantidade());
        String pre = String.valueOf(oibdistribuidora.getOibPreco());

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtEntrega.setText(formato.format(oibdistribuidora.getOibTempoEntrega()));

        jTxtCodigo.setText(cad);
        jTxtQuantidade.setText(quan);
        jTxtPreco.setText(pre);
        jTxtNome.setText(oibdistribuidora.getOibNome());
        jTxtCarro.setText(oibdistribuidora.getOibCarro());

    }

