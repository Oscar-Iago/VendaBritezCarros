    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        if (Util.perguntar("Deseja excluir o registro?") == true) {
            oibdistribuidora = viewBean();
            distribuidoraDAO.delete(oibdistribuidora);
        } else {

            Util.mensagem("Exclusão cancelada.");
        }

        Util.limparCampos(jTxtCodigo, jTxtNome, jTxtCarro, jFmtEntrega, jTxtQuantidade, jTxtPreco);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        oibdistribuidora = viewBean();
        if (incluindo == true) {
            distribuidoraDAO.insert(oibdistribuidora);
        } else {
            distribuidoraDAO.update(oibdistribuidora);
        }

        Util.habilitar(false, jTxtCodigo, jTxtNome, jTxtCarro, jFmtEntrega, jTxtQuantidade,
                jTxtPreco, jBtnCancelar, jBtnConfirmar);
        Util.habilitar(true, jBtnIncluir, jBtnAlterar, jBtnExcluir, jBtnPesquisar);
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

