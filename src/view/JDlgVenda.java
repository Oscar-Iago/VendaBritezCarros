    public Venda viewBean() {
        Venda venda = new Venda();
        int id = Integer.parseInt(jTxtCodigo.getText());
        int total = Integer.parseInt(jTxtTotal.getText());
        venda.setIdVenda(id);
        venda.setTotal(total);
        String pa = jCboPagamento.getSelectedItem().toString();
        venda.setForma_pagamento(pa);

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        try {
            venda.setData(formato.parse(jFmtData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }

        Clientes clientes1 = (Clientes) jCboCliente.getSelectedItem();
        venda.setCliente(clientes1.getIdClientes());

        Vendedor vendedor1 = (Vendedor) jCboVendedor.getSelectedItem();
        venda.setVendedor(vendedor1.getidVendedor());
        return venda;
    }

    public void beanView(Venda venda) {
        String id = String.valueOf(venda.getIdVenda());
        jTxtCodigo.setText(id);
        jTxtTotal.setText(String.valueOf(venda.getTotal()));
        jCboPagamento.setSelectedItem(venda.getForma_pagamento());

        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtData.setText(formato.format(venda.getData()));

        int idCliente = venda.getCliente();
        ClientesDAO clientesDAO = new ClientesDAO();
        jCboCliente.setSelectedItem((Clientes) clientesDAO.list(idCliente));

        int idVendedor = venda.getVendedor();
        VendedorDAO vendedorDAO = new VendedorDAO();
        jCboVendedor.setSelectedItem((Vendedor) vendedorDAO.list(idVendedor));
    }

