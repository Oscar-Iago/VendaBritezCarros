    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        int resp = JOptionPane.showConfirmDialog(null, "confirma exclusÃ£o ?",
                "Pergunta", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            Venda venda = viewBean();
            VendaDAO vendaDAO = new VendaDAO();
            vendaDAO.delete(venda);
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "ExclusÃ£o cancelada");
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        Venda venda = viewBean();
        VendaDAO vendaDAO = new VendaDAO();
        if (incluindo == true) {
            vendaDAO.insert(venda);
        } else {
            vendaDAO.update(venda);
            JOptionPane.showMessageDialog(null, "AlteraÃ§Ã£o feita com sucesso!.");
        }
        desabilitar();
        limparCampos();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

