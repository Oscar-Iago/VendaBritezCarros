    public OibProduto viewBean() {
        OibProduto oibproduto = new OibProduto();
        int id = Integer.valueOf(jTxtCodigo.getText());
        oibproduto.setIdProduto(id);
        oibproduto.setModelo(jTxtModelo.getText());
        oibproduto.setMarca(jTxtMarca.getText());
        SimpleDateFormat df = new SimpleDateFormat("dd/mm/yyyy");
        try {
            oibproduto.setChegada(df.parse(jFmtChegada.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(JDlgProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        oibproduto.setCor(jTxtCor.getText());
        //oibproduto.setPreco(Integer.valueOf(jTxtPreco.getText()));
        return oibproduto;
    }

    public void beanView(OibProduto oibproduto) {
        String cad = String.valueOf(oibproduto.getIdProduto());
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
        jFmtChegada.setText(formato.format(oibproduto.getChegada()));
        jTxtCodigo.setText(cad);
        jTxtModelo.setText(String.valueOf(oibproduto.getModelo()));
        jTxtMarca.setText(oibproduto.getMarca());
        jTxtCor.setText(oibproduto.getCor());
        //jTxtPreco.setText(Integer.toString(oibproduto.getPreco()));
    }

